package com.example.serviceprovider.Models;

public class ServiceHistory {
    private String service,amount,name,status,uid,id,address,phone;

    public ServiceHistory() {
    }

    public ServiceHistory(String service, String amount, String name, String status, String uid, String id, String address, String phone) {
        this.service = service;
        this.amount = amount;
        this.name = name;
        this.status = status;
        this.uid = uid;
        this.id = id;
        this.address = address;
        this.phone = phone;
    }

    public String getService() {
        return service;
    }

    public void setService(String service) {
        this.service = service;
    }

    public String getAmount() {
        return amount;
    }

    public void setAmount(String amount) {
        this.amount = amount;
    }

    public String getName() {
        return name;
    }

    public void setName(String name) {
        this.name = name;
    }

    public String getStatus() {
        return status;
    }

    public void setStatus(String status) {
        this.status = status;
    }

    public String getUid() {
        return uid;
    }

    public void setUid(String uid) {
        this.uid = uid;
    }

    public String getId() {
        return id;
    }

    public void setId(String id) {
        this.id = id;
    }

    public String getAddress() {
        return address;
    }

    public void setAddress(String address) {
        this.address = address;
    }

    public String getPhone() {
        return phone;
    }

    public void setPhone(String phone) {
        this.phone = phone;
    }
}
